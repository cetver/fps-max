<?php declare(strict_types=1);

namespace App\Dto;

/**
 * The "PandaScoreTeamDto" class
 */
final class PandaScoreTeamDto
{
    private int $id;
    private string $name;

    public function __construct(int $id, string $name)
    {
        $this->id = $id;
        $this->name = $name;
    }

    /**
     * @return int
     */
    public function getId(): int
    {
        return $this->id;
    }

    /**
     * @return string
     */
    public function getName(): string
    {
        return $this->name;
    }
}
